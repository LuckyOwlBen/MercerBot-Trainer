package com.java.mercerBot.data;

public class Features {

	private String feature_idx;

	private String name;

    private Type type;

	public String getFeature_idx() {
		return feature_idx;
	}
	public void setFeature_idx(String feature_idx) {
		this.feature_idx = feature_idx;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Type getType() {
		return type;
	}
	public void setType(Type type) {
		this.type = type;
	}
}
